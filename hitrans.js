function string2code(str){
  var output = "";
  for( var i=0; i<str.length; i++) {
    output += str.charCodeAt(i) + ',';
  }
  return output.substr(0, output.length -1);
}
function code2string(str){
  tokens = str.split(',');
  var output = "";
  for( var i=0; i<tokens.length; i++) {
    output += String.fromCharCode(tokens[i]);
  }
  return output;
}

